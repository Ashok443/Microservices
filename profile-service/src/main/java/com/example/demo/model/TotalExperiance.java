package com.example.demo.model;

public class TotalExperiance {

	private int inYears;
	private int inMonths;
	
	
	public TotalExperiance(int inYears, int inMonths) {
		super();
		this.inYears = inYears;
		this.inMonths = inMonths;
	}
	public int getInYears() {
		return inYears;
	}
	public void setInYears(int inYears) {
		this.inYears = inYears;
	}
	public int getInMonths() {
		return inMonths;
	}
	public void setInMonths(int inMonths) {
		this.inMonths = inMonths;
	}
	
	
}
